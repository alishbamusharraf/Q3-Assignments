# 2. Assignment Operators

# Assignment operator (=)
x = 10  # Assign 10 to variable x
print("x =", x)

# Add and assign (+=)
x += 5  
print("x after x += 5:", x)

# Subtract and assign (-=)
x -= 3  
print("x after x -= 3:", x)

# Multiply and assign (*=)
x *= 2  
print("x after x *= 2:", x)

# Divide and assign (/=)
x /= 4 
print("x after x /= 4:", x)

# Floor divide and assign (//=)
x //= 2  
print("x after x //= 2:", x)

# Modulus and assign (%=)
x %= 3 
print("x after x %= 3:", x)

# Exponentiate and assign (**=)
x **= 2  
print("x after x **= 2:", x)