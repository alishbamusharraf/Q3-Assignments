# 1. Arithmetic Operators:

# Addition (+)
a = 7
b = 3
result = a + b
print(f"a + b = {result}")  

# Subtraction (-)
result = a - b
print(f"a - b = {result}")  

# Multiplication (*)
result = a * b
print(f"a * b = {result}")  

# Division (/)
result = a / b
print(f"a / b = {result}")  

# Floor Division (//)
result = a // b
print(f"a // b = {result}")  

# Modulus (%)
result = a % b
print(f"a % b = {result}")  

# Exponentiation ()
result = a ** b
print(f"a ** b = {result}")  

# Using negative numbers
a = -10
b = 3
result = a + b
print(f"a + b = {result}")  

result = a * b
print(f"a * b = {result}") 


