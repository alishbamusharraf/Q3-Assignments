# 3.Comparison Operators

a = 7
b = 3
c = 7

# Equal to (==)
result = a == b
print(f"a == b: {result}")  

result = a == c
print(f"a == c: {result}") 

# Not equal to (!=)
result = a != b
print(f"a != b: {result}")  

result = a != c
print(f"a != c: {result}")  

# Greater than (>)
result = a > b
print(f"a > b: {result}")  

result = b > a
print(f"b > a: {result}")  

# Less than (<)
result = a < b
print(f"a < b: {result}")  

result = b < a
print(f"b < a: {result}")  

# Greater than or equal to (>=)
result = a >= b
print(f"a >= b: {result}")  

result = a >= c
print(f"a >= c: {result}") 

# Less than or equal to (<=)
result = a <= b
print(f"a <= b: {result}")  

result = b <= a
print(f"b <= a: {result}") 