# Data Types 

# Text Type
# <class 'str'>
message = "Hello, sir!"
print(message)  

# Numeric Types
# <class 'int'>
x = 7
print(x)  

# <class 'float'>
y = 7.7
print(y)  

# Sequence Types
# <class 'list'>
fruits= ["Cheery","Mango","Grapes"]
print(fruits)  

# <class 'tuple'>
my_tuple = (1, 2, 3,)
print(my_tuple) 

# Mapping Type
# <class 'dict'>
Introduction = {'name': 'Alishba', 'age': 18}
print(Introduction)  

# Set Types
# <class 'set'>
my_set = {1, 2, 3, 4, 5}
print(my_set)

# <class 'frozenset'>
my_frozenset = frozenset([1, 2, 3, 4])
print(my_frozenset)  

# Boolean
# <class 'bool'>
is_active = False
print(is_active)  
is_active = True
print(is_active)

# None Type
# <class 'NoneType'>
student = None
print(student) 
